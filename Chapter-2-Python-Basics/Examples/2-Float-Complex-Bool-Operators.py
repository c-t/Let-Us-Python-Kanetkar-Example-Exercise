# Deomnstrate use of float, complex and bool types and operators that can be used on them

# use of float
i = 3.5
j = 1.2
print(i%j)

# use of complex
a = 1 + 2j
b = 3 * (1 + 2j)
c = a * b
print(a)
print(b)
print(c)
print(a.real)
print(a.imag)
print(a.conjugate())

# use of bool
x = True
y = 3 > 4
print(x)
print(y)
